DATAS SEGMENT  
    SUR DB 0,-1,07FH,07FH,07FH,07FH,07FH,07FH,07FH,07FH
    N EQU $-SUR
    CON1 DB ?
    CON2 DB ?
    CON3 DB ?
DATAS ENDS

