    ASSUME CS:CODES,DS:DATAS
START:
	MOV AX,DATAS
    MOV DS,AX
  
    LEA SI,SUR
    MOV CX,N
LOP:  MOV BL,[SI]
    CMP BL,080H
    JA NEXT1
    CMP BL,000H
    JA NEXT2
    INC CON3
    JMP EXIT
    
NEXT1:  INC CON2
    JMP EXIT
NEXT2:  INC CON1
EXIT:   INC SI
    LOOP LOP
    
    MOV AH,4CH   
    INT 21H
CODES ENDS
    END START