DATAS SEGMENT  
    DAT DB 0,-1,1,123,2,23,34,24,234,13,3,4,5,6,9,7
    N EQU $-DAT
    RESULT DB N DUP(?)
DATAS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS
START:
	MOV AX,DATAS
    MOV DS,AX
    
    MOV CX,N
    LEA SI,DAT
    LEA DI,RESULT
LOP:
    MOV AL,[SI]
    MOV [DI],AL
    INC SI
    INC DI
    LOOP LOP
  
    MOV CX,N
    DEC CX
LOP1:
    MOV DI,CX
    MOV BX,0
LOP2:
    MOV AL,RESULT[BX]
    CMP AL,RESULT[BX+1]
    JLE CONTIMUE
    XCHG AL,RESULT[BX+1]
    MOV RESULT[BX],AL
CONTIMUE:
    ADD BX,1
    LOOP LOP2
    MOV CX,DI
    LOOP LOP1
    
    MOV AH,4CH   
    INT 21H
CODES ENDS
    END START